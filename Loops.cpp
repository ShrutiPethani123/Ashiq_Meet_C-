#include<iostream>
using namespace std;

int main()
{
    for(int i=20;i<=30;i++)
    {
        cout<<i<<" ";
    }
    cout<<endl;
    
    int i=20;
    while(i<=30)
    {
        cout<<i<<" ";
        i++;
    }
    cout<<endl;
    int j=20;
    do{
        cout<<j<<" ";
        j++;
    }while(j<=30);
}

